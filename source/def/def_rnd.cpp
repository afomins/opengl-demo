t_double crt_rnd( )
{
  t_long rnd = RAND_MAX - rand( );

  /* ............................................................................ */

  return rnd / ( t_double )RAND_MAX;
}

/* ------------------------------------------------------------------------------ */

t_long crt_rnd( t_long delta )
{
  return ( t_long )( delta * crt_rnd( ) );
}
